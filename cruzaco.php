                  <h3>Moldagem Mecanizada:</h3>
                  <div class="imgdireita"><img src="img/cruzaco02_s1.jpg" alt=""></div>
                  <p>Para a produção de peças seriadas com peso unitário de até 800kg. A Cruzaço conta com máquinas de moldar de alta capacidade produtiva, atendendo a diversos perfis. </p>
                  <h3>Moldagem Manual: </h3>
                  <p>Peças com maiores dimensões, com peso de até 20.000kg, são moldadas manualmente pelo processo de cura a frio, utilizando misturadores contínuos de grande porte.</p>
                  <h3>Fusão:</h3>
                  <p>2 fornos elétricos a arco, trifásicos, com capacidade nominal de 34t/corrida. </p>
