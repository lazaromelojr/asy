            	<div class="titulo">ASY Trading oferece a oportunidade de negócios na malha ferroviária de Cuba</div> 
              <p>A ASY está negociando o fornecimento de vários produtos e equipamentos relacionados ao setor ferroviário de Cuba <a href="contato.php">(clique aqui para mais informações)</a>. A partir dessas negociações, surgiu a oportunidade de estabelecer no país um centro de manutenção, renovação e modernização da malha ferroviária cubana, com a participação direta de empresas brasileiras que atuam no setor. </p>
              <p>A ASY é parte do grupo WAD Equipamentos Industriais <a href="http://www.wadequipamentos.com.br/" target="_black">(www.wadequipamentos.com.br)</a>, que tem expertise de atuação nos setores industriais, sobretudo na siderurgia e na mineração — razão pela qual tal oportunidade (advinda durante reuniões mantidas com autoridades e órgãos da administração cubana, principalmente, com o Ministério de Transportes) foi prontamente objeto de interesse da trading. </p>
              <p>O trabalho em Cuba não é, de fato, nenhuma novidade para a corporação. Com mais de 20 anos de mercado, a ASY possui vasta atuação e experiência na manutenção, reparo, instalação e suprimento de peças e equipamentos junto aos canteiros de obras e às instalações extrativas e fabris de siderurgias e mineradoras em todo o Brasil. </p>
              <p>O negócio no país cubano, por sua vez, consiste na recuperação dos 12 mil quilômetros da malha ferroviária de Cuba e na implantação de um espaço, no qual as empresas participantes receberão vagões, locomotivas e equipamentos ferroviários em geral, para manutenção, reparos e modernização. As empresas que poderão participar dessa oportunidade serão todas brasileiras, já que o Brasil conta com expressivo transporte ferroviário pesado (como o de minério) pela malha ferroviária do país. Exatamente por isso, a modernização em terras tupiniquins foi necessária e é contínua. <a href="manutencaoTrens.php">(Veja aqui as fotos da nossa recente visita as instalações da oficina de manutenção para Trens de Passageiros Cubano.)</a>   </p>
              <p>Quaisquer empresas com capacitação e atuação no mercado ferroviário (seja na fabricação de locomotivas, vagões, motores, trilhos, dormentes, no desenvolvimento de softwares, projetos e de construção, por exemplo) podem (e devem) se integrar ao projeto. A iniciativa e o início das negociações da ASY com o governo cubano começaram faz nove meses, mais precisamente, após o anúncio, por parte do presidente dos EUA, Barack Obama, da intenção de reatar relações diplomáticas entre ambos os países e encerrar o bloqueio comercial, financeiro e econômico imposto pelos Estados Unidos a Cuba, há mais de 50 anos.</p> 
              <p>Com a sinalização da quebra do bloqueio imposto a Cuba, o país, certamente, abrirá as portas para o mundo, e a atualização da malha e dos equipamentos ferroviários será um passo fundamental para esse novo processo. O papel da ASY é trazer essa oportunidade ao conhecimento das empresas com o perfil adequado, que tenham interesse em fazer parte de tal parceria e de integrar esse centro de prestação de serviço. </p>
              <p>Trata-se de um negócio de grande porte, já que a extensão da malha ferroviária é relevante (12 mil quilômetros), e o longo período, de mais de 50 anos, em que tais cuidados não foram tomados foi prejudicial. Além disso, a gestão é contínua, uma vez que, nesse setor, a modernização constante é fundamental.</p>
              <p>Logo após o anúncio, a ASY, juntamente com a <a href="../cuba.php">MWL </a>– empresa Brasileira pertencente ao grupo alemão GMH, que fabrica em Caçapava (SP), rodas e eixos ferroviários – começou o desenvolvimento do projeto de fornecimento de produtos ferroviários a Cuba. A primeira fase foi a de pesquisa de dados sobre a ilha, sua economia e regulamentações. Depois, foi fundamental a busca de um profissional local com competência e condições de oferecer o suporte logístico para os contatos com os órgãos e autoridades cubanas.</p>
              <p>Assim, antes mesmo da concretização da reabertura das embaixadas, os executivos da ASY já estavam em Cuba, negociando a exportação desses produtos com as autoridades responsáveis pelo setor ferroviário e de suprimento da ilha. Hoje, é possível falar que guardamos, em terras brasileiras, competentes e importantes empresas de atuação no setor, tais quais: retíficas e manutenção de motores de locomotivas, compressores e sistemas de frenagem, sirenes e apitos, reforma e reparação de vagões de carga e de passageiros, caldeiraria chaparia, etc., além de guindastes e equipamentos de socorro e de serviços em geral — ou seja, de toda a infraestrutura de ferrovias, do controle, da sinalização e das obras de arte, como pontes, túneis e viadutos. </p>
                            
              <p>Hoje, passados cerca de um ano e meio do início dos negócios em Cuba, outras empresas já entraram ou estão se preparando para entrar neste mercado promissor, tais como:
                <a href="../kintel.php">Kintel,</a> fabrica em São Bernardo do Campo peças sob encomenda para locomotivas, assim como prestação de serviços de manutenção de conjuntos de força para motores EMD e GE;brasileiras;
                <a href="../hewitt.php">Hewitt</a>, fabrica em Hortolândia, SP, truques, via permanente e diversos outros equipamentos para a malha ferroviária;
                <a href="../lubem.php">Lubem</a> traz uma novidade para a lubrificação do contato roda/trilho: um bico injetor embarcado na locomotiva, que injeta nas rodas da locomotiva um preparado de grafite, que elimina as graxeiras fixas na via férrea;
                <a href="../cruzaco.php">Cruzaço</a>, fabrica em Jarinu, SP, diversos componentes fundidos para o setor ferroviário, tais como armação para truques, engates, cruxetas, bolsters, braçadeiras e inúmeras outras peças.</p>


