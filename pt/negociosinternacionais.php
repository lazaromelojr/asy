              <h4>Programa de Desenvolvimento de Negócios Internacionais</h4>
              <p>A ASY atua neste segmento de negócios desde a percepção da WBC desta oportunidade há cerca de 20 anos, quando foi procurada pela agência de desenvolvimento de negócios do governo holandês, o CBI (Centre for the Promotion of Developing Countries), interessado em utilizar nossos serviços para desenvolver fornecedores de produtos e serviços demandados por investidores e empresas holandesas.</p>
              <p>Posteriormente, em junho de 2004, foi desenvolvida uma parceria com o ITC (International Trade Center), um organismo resultante da cooperação entre a WTO (Organização Internacional do Comércio) e a UNCTAD (Conferência das Nações Unidas para o Desenvolvimento do Comércio), sediado em Genebra, Suíça.</p>
              <p>Uma das fases importantes do conhecimento do mercado internacional de investimentos e joint-ventures são as feiras industriais ao redor do mundo, como a de Hannover, Alemanha, na qual a WBC/ASY iniciou sua participação nos pavilhões da Holanda, dentro dos programas de desenvolvimento de negócios e de fornecedores da agência holandesa. Isso nos possibilitou desenvolver um know how próprio, obtido com essa parceria de tantos anos com o CBI ─ know how esse sedimentado mais recentemente com os fundamentos dos programas da ITC.</p>
              <p>A partir desse conhecimento, passamos a preparar empresas e empresários brasileiros interessados em buscar negócios no exterior, sejam estes de investimento, capital estrangeiro, transferência de tecnologia, financiamentos, parcerias e joint-ventures em geral, utilizando, para isso, as facilidades oferecidas pelo GB&M (Global Business & Markets) ─ <a href="http://www.hannovermesse.de/home" target="_branck"> www.hannovermesse.de </a> => Events => Global Business and Markets ─, que ocorre dentro do ambiente da Feira de Hannover, na Alemanha, todos os anos nos meses de abril. </p>
              
