              <p>Multi row cylindrical roller bearings are mostly used for the roll necks of rolling mill stands and roller presses.</p>
              <p>To meet the challenging requirements of the rolled products plants these bearings must ensure a remarkable running accuracy and the capability to operate with heavy radial loads and high speeds. </p><br><br>

