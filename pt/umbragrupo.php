              <h2>Ball Bearings Single Row</h2>

              <div class="img_corpo" align="center"><img src="../img/umbragroup/08.png" height="150" width="150" alt=""></div>
              <h3>Angular Contacts Ball Bearings</h3>
              <p>These bearings are designed to support combined radial and axial loads. The bearings may be produced with different contact angles.</p>
              <p>The load capacity and the service speeds vary depending on the contact angle.</p>
              <p>They are normally mounted in pair/set. </p><br><br>

              <div class="fiocinza"></div>

              <div class="img_corpo" align="center"><img src="../img/umbragroup/09.png" height="150" width="150" alt=""></div>
              <h3>Deep Groove Ball Bearings</h3>
              <p>They are highly versatile bearings used in a wide range of applications. They can withstand radial as well as axial loads in both directions. Suitable for high-speed applications.</p><br><br><br><br><br>

              <div class="fiocinza"></div>

              <div class="img_corpo" align="center"><img src="../img/umbragroup/10.png" height="150" width="180" alt=""></div>
              <h3>Four Point Contact Ball Bearings</h3>
              <p>These bearings can support radial loads and axial loads in both directions and so they can replace a pair/set  of angular contact bearings with a reduced axial dimension.</p>
              <p>Four point contact ball bearings usually have one  ring made of two halves; this allows to locate a larger number of balls and hence increase the load capacity.</p><br>

              <div class="fiocinza"></div>

              <div class="img_corpo" align="center"><img src="../img/umbragroup/11.png" height="150" width="150" alt=""></div>
              <h3>Spindle Bearings</h3>
              <p>Spindle bearings are angular contact ball bearings with extremely restricted tolerances. They are particularly suitable for support systems which require the maximum guiding precision  and the capacity to reach high rotational speeds. These bearings are mainly used to support machine tool spindles.</p>
              <p>The cages are normally made of plastic material (phenolic resin, peek, polyamide etc.) which has a low friction coefficient, is very light and is hence particularly suited to high speeds.</p><br><br>

              <div class="fiocinza"></div>
              <div class="img_corpo" align="center"><img src="../img/umbragroup/12.png" height="150" width="150" alt=""></div>
              <h2>Ball Bearings Double Row</h2>
              <h3>2 Rows Self Aligning Ball Bearings</h3>
              <p>These are non-separable two rows  radial bearings. The outer ring’s spherical race allows to compensate possible misalignments.</p>
              <p>Ideal for low radial and axial loads. Also available in taper bore version.</p><br>

              <div class="fiocinza"></div>
              <h2>Cylindrical Roller Bearings Single Row</h2>
              <div class="img_corpo" align="center"><img src="../img/umbragroup/13.png" height="150" width="150" alt=""></div>
              <h3>Series N, NU, NJ, NUP, Full Complement</h3>
              <p>The cylindrical roller bearings are usually used to accommodate large radial loads and they can also reach relatively high speeds. These bearings are separable with consequent advantages in the assembly/disassembly.</p>
              <p>There are different design types in which retainer shoulders are present on one or on both rings.</p>
              <p>N and NU may be used as a floating bearing and can compensate the position changes between shaft and housing due to the thermal expansion.</p>
              <p>NJ and NUP provide axial location and can support also small axial loads (one and both directions)</p>
              <p>These bearings feature solid brass or pressed steel cages.</p>

              <div class="fiocinza"></div>
              <h2>Combined Axial/Radial Load Bearings</h2>
              <div class="img_corpo" align="center"><img src="../img/umbragroup/14.png" height="150" width="150" alt=""></div>
              <h3>Combined Axial/Radial Load Bearings</h3>
              <p>The axial/radial bearings  are used, due to their accuracy, for rotating and indexing tables and milling heads. Radial forces, axial forces in both directions and tilting moments can be withstood.</p>
              <p>These bearings are usually preloaded to increase the system stiffness.</p><br><br>

              <div class="fiocinza"></div>
              <h2>Tapered Roller Bearings</h2>
              <div class="img_corpo" align="center"><img src="../img/umbragroup/15.png" height="150" width="150" alt=""></div>
              <h3>Tapered Roller Bearings</h3>
              <p>These bearings can withstand  high radial and axial loads.</p>
              <p>In general they are used in pairs/set and properly adjusted in order to achieve the required radial and axial clearance/preload condition.</p>
              <p>One, two or four row configurations are available; these bearing types are usually equipped with pressed steel cages.</p><br><br>


              <div class="fiocinza"></div>
